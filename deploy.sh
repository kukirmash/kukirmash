#!/bin/bash

# Переходим в директорию, где лежит сам скрипт
cd "$(dirname "$0")" || exit

# ==========================================
# ШАГ 0: ПРОВЕРКА DOCKER
# ==========================================
if ! docker info > /dev/null 2>&1; then
    echo "⏳ Docker не запущен. Пытаюсь запустить Docker Desktop..."
    
    # Команда запуска Docker Desktop для Linux
    systemctl --user start docker-desktop
    
    # Ждем 5 секунд, чтобы демон успел инициализироваться
    sleep 5
    
    # Проверяем еще раз
    if ! docker info > /dev/null 2>&1; then
        echo "❌ Не удалось запустить Docker. Пожалуйста, запусти Docker Desktop вручную."
        exit 1
    fi
    echo "✅ Docker успешно запущен!"
fi

# ==========================================
# ШАГ 1: ОСТАНОВКА
# ==========================================
echo "🐳 Останавливаем старые контейнеры..."
docker compose down

# ==========================================
# ШАГ 2: СБОРКА ФРОНТЕНДА
# ==========================================
echo "📦 Собираем React (Frontend)..."
cd kukirmash_frontend || { echo "❌ Папка kukirmash_frontend не найдена!"; exit 1; }

# Устанавливаем зависимости (если появились новые) и собираем билд
npm install
npm run build

# Возвращаемся обратно в корень
cd ..
echo "✅ Фронтенд успешно собран!"

# ==========================================
# ШАГ 3: ЗАПУСК DOCKER COMPOSE
# ==========================================
echo "🚀 Собираем и запускаем проект..."
docker compose up --build -d

if [ $? -eq 0 ]; then
    echo "========================================="
    echo "✅ УСПЕХ! Проект запущен в Docker."
    echo "========================================="
    echo "🌐 Фронтенд: http://localhost:3000"
    echo "⚙️  Бэкенд:   http://localhost:5000/swagger"
    echo "========================================="
else
    echo "❌ Произошла ошибка при запуске контейнеров."
    exit 1
fi
